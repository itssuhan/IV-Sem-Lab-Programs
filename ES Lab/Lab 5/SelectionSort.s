	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA MYCODE, CODE, READONLY
ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=ARR
	MOV R1,#4
OUTLOOP
	LDR R2,[R0]
	MOV R3,R1
	ADD R4,R0,#4
INLOOP
	LDR R2,[R0]
	LDR R5,[R4]
	CMP R2,R5
	BGT SWAP
RETURN
	SUBS R3,#1
	ADD R4,#4
	BNE INLOOP
	SUBS R1,#1
	ADD R0,#4
	BNE OUTLOOP
STOP
	B STOP
SWAP
	STR R5,[R0]
	STR R2,[R4]
	B RETURN
	AREA MYDATA, DATA, READWRITE
ARR DCD 0x0
	END
