	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
ENTRY
	EXPORT Reset_Handler
Reset_Handler
NUM EQU 0XFFF
	MOV R2,#0
	MOV R3,#0
	MOV R4,#0
	LDR R1,=NUM
LOOP
	CMP R1, #1000
	BLT NEXT
	SUB R1, R1, #1000
	ADD R2,R2, #1
	B LOOP
NEXT 
	CMP R1, #100
	BLT NEXT1
	SUB R1,R1, #100
	ADD R3, R3,#1
	B NEXT
NEXT1
	CMP R1, #10
	BLT NEXT2
	SUB R1, R1, #10
	ADD R4,R4, #1
	B NEXT1
NEXT2
	LDR R5,=RESULT
	ADD R1,R4,LSL#4
	ADD R3,R2, LSL#4
	STRB R1,[R5],#1
	STRB R3,[R5]
STOP
	B STOP
RESULT DCD 0
	AREA mydata,DATA,READWRITE
	END